#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;
	int n;

	i = 0;
	n = 0;
	while (s[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (s[i] == c)
			return ((char *)s + i);
		i--;
	}
	if (s[i] == '\0' && c == '\0')
		return ((char *)s + i);
	return (0);
}
